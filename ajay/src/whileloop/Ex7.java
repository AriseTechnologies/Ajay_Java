package whileloop;

public class Ex7 {
public static void main(String[] args) {
	int n=45;
	System.out.println("Reverse order");
	int  i=-45;
	 while (i<=-1) {
		 System.out.println(i);
		 i++;
	 }
	System.out.println("forward order");
	 i=1;
	while (i<=n) {
		System.out.println(i);
		i++;
	}
	
	
}
}
